const { execSync } = require("node:child_process");
const fs = require("node:fs");
const path = require("node:path");

const root = path.resolve(__dirname, "..");
const bundleRoot = path.join(root, "src-tauri", "target", "release", "bundle");

function argValue(name, fallback = null) {
  const index = process.argv.indexOf(name);
  return index >= 0 ? process.argv[index + 1] : fallback;
}

function hasArg(name) {
  return process.argv.includes(name);
}

function readJson(file) {
  return JSON.parse(fs.readFileSync(file, "utf8"));
}

function walk(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const full = path.join(dir, entry.name);
    return entry.isDirectory() ? walk(full) : [full];
  });
}

function detectGithubRepo() {
  if (process.env.GITHUB_REPOSITORY) return process.env.GITHUB_REPOSITORY;
  try {
    const remote = execSync("git config --get remote.origin.url", {
      cwd: root,
      stdio: ["ignore", "pipe", "ignore"],
      encoding: "utf8",
    }).trim();
    const match = remote.match(/github\.com[:/](.+?\/.+?)(?:\.git)?$/i);
    return match ? match[1] : null;
  } catch {
    return null;
  }
}

function encodeAssetName(name) {
  return encodeURIComponent(name);
}

function releaseAssetName(name) {
  return name.replace(/\s+/g, ".");
}

function artifactKind(file) {
  const lower = file.toLowerCase();
  if (lower.endsWith(".exe")) return "nsis";
  if (lower.endsWith(".msi")) return "msi";
  return null;
}

const pkg = readJson(path.join(root, "package.json"));
const version = argValue("--version", process.env.PANTHER_RELEASE_VERSION || pkg.version);
const local = hasArg("--local");
const feedDir = path.join(root, "release", local ? "update-feed-local" : "update-feed");
const port = argValue("--port", process.env.PANTHER_UPDATE_TEST_PORT || "17632");
const repo = argValue("--repo", process.env.PANTHER_GITHUB_REPOSITORY || detectGithubRepo() || "panthercityhelpersfw/panther-studio");
const tag = argValue("--tag", process.env.PANTHER_RELEASE_TAG || `v${version}`);
const windowsInstaller = argValue("--installer", process.env.PANTHER_WINDOWS_UPDATER_INSTALLER || "nsis");
const notes =
  argValue("--notes") ||
  process.env.PANTHER_RELEASE_NOTES ||
  `Panther Studio ${version} update. Signed updater package generated by the Panther release pipeline.`;

const allFiles = walk(bundleRoot);
const artifacts = allFiles
  .filter((file) => {
    const kind = artifactKind(file);
    if (!kind || kind !== windowsInstaller) return false;
    if (!fs.existsSync(`${file}.sig`)) return false;
    return path.basename(file).includes(version);
  })
  .sort((a, b) => a.localeCompare(b));

if (!artifacts.length) {
  console.error(`No signed Windows ${windowsInstaller} updater artifact found for ${version} under ${bundleRoot}`);
  console.error("Run: npm run release:updater");
  process.exit(1);
}

fs.rmSync(feedDir, { recursive: true, force: true });
fs.mkdirSync(feedDir, { recursive: true });

const artifact = artifacts[0];
const name = path.basename(artifact);
const assetName = releaseAssetName(name);
const signature = fs.readFileSync(`${artifact}.sig`, "utf8").trim();

fs.copyFileSync(artifact, path.join(feedDir, assetName));
fs.copyFileSync(`${artifact}.sig`, path.join(feedDir, `${assetName}.sig`));

const url = local
  ? `http://127.0.0.1:${port}/${encodeURIComponent(assetName)}`
  : `https://github.com/${repo}/releases/download/${tag}/${encodeAssetName(assetName)}`;

const platforms = {
  "windows-x86_64": { signature, url },
};

const manifest = {
  version,
  notes,
  pub_date: new Date().toISOString(),
  platforms,
};

const manifestPath = path.join(feedDir, "latest.json");
fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);

console.log(`Wrote ${manifestPath}`);
console.log(`Selected Windows updater artifact: ${windowsInstaller}`);
console.log("Platforms:");
for (const key of Object.keys(platforms)) {
  console.log(`- ${key}: ${platforms[key].url}`);
}
